# ---------------------------------------------------------------------------


class TestTaskStatus:
    def test_creation(self) -> None:
        status = TaskStatus(id=1, title="Test", status="pending")
        assert status.id == 1
        assert status.title == "Test"
        assert status.status == "pending"
        assert status.description == ""

    def test_with_description(self) -> None:
        status = TaskStatus(id=1, title="Test", status="done", description="All done")
        assert status.description == "All done"


