"""Tests for task state tracking."""

from __future__ import annotations

from typing import TYPE_CHECKING

import pytest

if TYPE_CHECKING:
    from pathlib import Path

from intake.utils.task_state import (
    VALID_STATUSES,
    TaskStateError,
    TaskStateManager,
    TaskStatus,
)

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

SAMPLE_TASKS_MD = """\
# Implementation Tasks

> Auto-generated by intake. Do not edit manually.

| ID | Title | Status | Est. Minutes | Dependencies |
|----|-------|--------|-------------|--------------|
| 1 | Set up project structure | pending | 15 | none |
| 2 | Implement authentication | pending | 30 | 1 |
| 3 | Write tests | pending | 20 | 1, 2 |

---

## Task 1: Set up project structure

Create the initial project directory layout.

**Status:** pending
**Estimated time:** 15 minutes

**Files:**
- `src/app.py`

## Task 2: Implement authentication

Add OAuth2 authentication flow.

**Status:** pending
**Estimated time:** 30 minutes
**Depends on:** 1

**Files:**
- `src/auth.py`
- `src/middleware.py`

## Task 3: Write tests

Write unit and integration tests.

**Status:** pending
**Estimated time:** 20 minutes
**Depends on:** 1, 2

**Files:**
- `tests/test_auth.py`
"""


def _create_spec_dir(tmp_path: Path, content: str = SAMPLE_TASKS_MD) -> Path:
    """Create a spec directory with a tasks.md file."""
    spec_dir = tmp_path / "test-spec"
    spec_dir.mkdir()
    (spec_dir / "tasks.md").write_text(content, encoding="utf-8")
    return spec_dir


